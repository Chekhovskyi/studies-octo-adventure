SELECT  Клуби.Назва,
        SUM(IIF ( Статистика.Статус = 'W', 1, 0)) AS Перемоги,
        SUM(IIF ( Статистика.Статус = 'L', 1, 0)) AS Поразки,
        SUM(IIF ( Статистика.Статус = 'P', 1, 0)) AS Нічия
FROM    Статистика INNER JOIN
          Клуби ON Статистика.КлубІд = Клуби.Ід
GROUP BY Клуби.Назва
HAVING (SUM(Клуби.Бюджет) > 1000000)
ORDER BY Клуби.Назва
